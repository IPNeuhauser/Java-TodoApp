    private void jLabelProjectDeletMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelProjectDeletMouseClicked
        Project project = (Project) projectModel.get(jListProjects.getSelectedIndex());
        DeletProjectDialogScreen deletProjectDialogScreen = new DeletProjectDialogScreen(this, rootPaneCheckingEnabled);
        deletProjectDialogScreen.setProject(project);
        deletProjectDialogScreen.setVisible(true);
        
        deletProjectDialogScreen.addWindowListener(new WindowAdapter(){
            @Override
            public void windowClosed(WindowEvent e){
                loadProjects();
                if(!projectModel.isEmpty()){
                    jListProjects.setSelectedIndex(0);
                    Project setProject = (Project) projectModel.get(0);
                    loadTasks(setProject.getId());
                }
            }
        });
    }//GEN-LAST:event_jLabelProjectDeletMouseClicked

