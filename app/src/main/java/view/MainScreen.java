    private void jTableTasksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTasksMouseClicked
        int rowIndex = jTableTasks.rowAtPoint(evt.getPoint());
        int columnIndex = jTableTasks.columnAtPoint(evt.getPoint());
        
        switch(columnIndex){
            case 3 -> {
                Task task = taskModel.getTasks().get(rowIndex);
                taskController.update(task);
            }
            case 4 -> {
                Task task = taskModel.getTasks().get(rowIndex);
                UpdateTaskDialogScreen updateTaskDialogScreen = new UpdateTaskDialogScreen(this, rootPaneCheckingEnabled);
                updateTaskDialogScreen.setTask(task);
                updateTaskDialogScreen.completField();
                updateTaskDialogScreen.setVisible(true);
                
                updateTaskDialogScreen.addWindowListener(new WindowAdapter(){
                    @Override
                    public void windowClosed(WindowEvent e){
                        Project project = (Project) projectModel.get(jListProjects.getSelectedIndex());
                        loadTasks(project.getId());
                    }
                });
            }

            case 5 -> {
                Task task = taskModel.getTasks().get(rowIndex);
                DeletTaskDialogScreen deletTaskDialogScreen = new DeletTaskDialogScreen(this, rootPaneCheckingEnabled);
                deletTaskDialogScreen.setTask(task);
                deletTaskDialogScreen.setTaskModel(taskModel);
                deletTaskDialogScreen.setVisible(true);
                
                
                deletTaskDialogScreen.addWindowListener(new WindowAdapter(){
                    @Override
                    public void windowClosed(WindowEvent e){
                        taskModel = deletTaskDialogScreen.getTaskModel();
                        Project project = (Project) projectModel.get(jListProjects.getSelectedIndex());
                        loadTasks(project.getId());
                    }
                });
                
            }
        }   
    }//GEN-LAST:event_jTableTasksMouseClicked

